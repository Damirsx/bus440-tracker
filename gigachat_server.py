# Кэш токена (чтобы не запрашивать каждый раз)
access_token_cache = {
    'token': None,
    'expires_at': 0
}


def get_access_token():
    """Получение токена доступа GigaChat"""
    import time
    
    # Проверяем кэш
    if access_token_cache['token'] and time.time() < access_token_cache['expires_at']:
        return access_token_cache['token']
    
    try:
        response = requests.post(
            GIGACHAT_AUTH_URL,
            headers={
                'Authorization': f'Bearer {GIGACHAT_API_KEY}',
                'RqUID': str(uuid.uuid4()),
                'Content-Type': 'application/x-www-form-urlencoded'
            },
            data={'scope': 'GIGACHAT_API_PERS'},
            verify=False,  # Для работы с сертификатами Сбера
            timeout=10
        )
        
        if response.status_code == 200:
            data = response.json()
            token = data.get('access_token')
            expires_in = data.get('expires_at', 1800000)  # По умолчанию 30 минут
            
            # Кэшируем токен
            access_token_cache['token'] = token
            access_token_cache['expires_at'] = time.time() + (expires_in / 1000) - 60  # За минуту до истечения
            
            return token
        else:
            print(f"Ошибка получения токена: {response.status_code}")
            print(response.text)
            return None
            
    except Exception as e:
        print(f"Исключение при получении токена: {e}")
        return None


def chat_with_gigachat(user_message, conversation_history=None):
    """Отправка сообщения в GigaChat и получение ответа"""
    
    # Получаем токен
    token = get_access_token()
    if not token:
        return "❌ Ошибка подключения к GigaChat. Проверьте API ключ."
    
    # Формируем системный промпт с текущим временем
    now = datetime.now()
    current_time = now.strftime("%H:%M")
    days = ['Понедельник', 'Вторник', 'Среда', 'Четверг', 'Пятница', 'Суббота', 'Воскресенье']
    day_of_week = days[now.weekday()]
    
    system_prompt = SYSTEM_PROMPT.format(
        current_time=current_time,
        day_of_week=day_of_week
    )
    
    # Формируем историю сообщений
    messages = [
        {
            "role": "system",
            "content": system_prompt
        }
    ]
    
    # Добавляем историю диалога если есть
    if conversation_history:
        messages.extend(conversation_history)
    
    # Добавляем текущее сообщение пользователя
    messages.append({
        "role": "user",
        "content": user_message
    })
    
    try:
        response = requests.post(
            GIGACHAT_API_URL,
            headers={
                'Authorization': f'Bearer {token}',
                'Content-Type': 'application/json'
            },
            json={
                'model': 'GigaChat',
                'messages': messages,
                'temperature': 0.7,
                'max_tokens': 500,
                'n': 1
            },
            verify=False,
            timeout=30
        )
        
        if response.status_code == 200:
            data = response.json()
            assistant_message = data['choices'][0]['message']['content']
            return assistant_message
        else:
            print(f"Ошибка GigaChat API: {response.status_code}")
            print(response.text)
            return f"❌ Ошибка GigaChat: {response.status_code}. Попробуйте позже."
            
    except requests.Timeout:
        return "⏱️ Превышено время ожидания. Попробуйте еще раз."
    except Exception as e:
        print(f"Исключение при запросе к GigaChat: {e}")
        return f"❌ Ошибка: {str(e)}"


@app.route('/chat', methods=['POST'])
def chat():
    """Endpoint для чата с AI"""
    try:
        data = request.json
        user_message = data.get('message', '')
        history = data.get('history', [])
        
        if not user_message:
            return jsonify({'error': 'Сообщение пустое'}), 400
        
        # Получаем ответ от GigaChat
        response = chat_with_gigachat(user_message, history)
        
        return jsonify({
            'response': response,
            'timestamp': datetime.now().isoformat()
        })
        
    except Exception as e:
        return jsonify({'error': str(e)}), 500


